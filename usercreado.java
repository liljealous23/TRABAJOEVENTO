    public boolean validar() {
        if (nombre.getText().equals("") || userc.getText().equals("") || contra1.getText().equals("") || edad1.getValue().equals("")) {
            if (nombre.getText().equals("")) {
                validacion1.setVisible(true);
            }
            if (userc.getText().equals("")) {
                validacion2.setVisible(true);
            }
            if (contra1.getText().equals("")) {
                validacion3.setVisible(true);
            }
            if (edad1.getValue().equals("0")) {
                validacion4.setVisible(true);
                return true;
            }
        }
        return false;
    }
    private void creandoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_creandoActionPerformed
        // TODO add your handling code here:

        String tipo = (String) rol.getSelectedItem();
        String users = userc.getText();
        String password = contra1.getText();
        String nombres = nombre.getText();
        int edad;

        edad = (Integer) edad1.getValue();
        if (validar() == false) {
            
            if(p.userunico(users)==true){
            if (tipo.equals("ADMINISTRADOR")) {

                p.agregar(tipo, nombres, users, password, edad);
                new crearuser().setVisible(true);
                this.dispose();
            } else if (tipo.equals("CONTENIDO")) {
                con.agregar(tipo, nombres, users, password, edad);
                new crearuser().setVisible(true);
                this.dispose();
            } else if (tipo.equals("LIMITADO")) {
                lim.agregar(tipo, nombres, users, password, edad);
                new crearuser().setVisible(true);
                this.dispose();
            }
        }else{
                   JOptionPane.showMessageDialog(this, "USERNAME YA ESTA EN UTILIZADO");
            }
        }
    }//GEN-LAST:event_creandoActionPerformed

